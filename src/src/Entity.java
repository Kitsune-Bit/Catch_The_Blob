package src;

public class Entity {

    private float speed;
    private float size;

    public Entity() {
        this.speed = 1;
        this.size = 1;
    }

    public float getSpeed() {
        return speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public float getSize() {
        return size;
    }

    public void setSize(float size) {
        this.size = size;
    }

}
